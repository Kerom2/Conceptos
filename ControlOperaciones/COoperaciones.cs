using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace ConceptosIniciales.ControlOperaciones
{
    class ProcesOperaciones
      
    {   //funcion que recibe dos enteros y devuelve la suma de los dos numeros.
        public int suma(int n1, int n2)
        {
            return n1 + n2;
        }

        //funcion que recibe dos enteris y devuelve la resta de los dos numeroas.
        public int resta(int n1, int n2)
        {
            return n1 - n2;
        }

        //funcion que recibe dos enteris y devuelve la ultiplicacion de los dos numeroas.
        public int mult(int n1, int n2)
        {
            return n1 * n2;
        }

        //funcion que recibe dos enteris y devuelve la division de los dos numeroas.
        public int div(int n1, int n2)
        {
            return n1 / n2;
        }
    }
}
